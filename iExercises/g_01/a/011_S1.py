# -----------------------------------------------------------------------------
# -- This file demonstrates how to print the documentation (docstring) of various Python built-in functions. Each function's docstring provides a description of its purpose and usage.
# -----------------------------------------------------------------------------

# Print the docstring (documentation) of the 'abs' function
print(abs.__doc__) # Return the absolute value of the argument.

# Print the docstring (documentation) of the 'len' function
print(len.__doc__) # Return the number of items in a container.

# Print the docstring (documentation) of the 'sorted' function
print(sorted.__doc__)