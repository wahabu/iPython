# Import the 'pi' constant from the 'math' module to calculate the volume of a sphere
from math import pi

# Define the radius of the sphere
r = 6.0

# Calculate the volume of the sphere using the formula
V = 4.0/3.0 * pi * r**3

print("The volume of the sphere is: ", V)